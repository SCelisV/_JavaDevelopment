package JavaTutorials.LanguageBasics;

public class valuesTest {

    public static void main(String[] args) {
        
            /*
             * utilizando la versión postfijo (i++)
           int i = 10;
           int n = i++%5; // 10%5 = 0

           System.out.println("El valor de i es: " + i); // El valor de i es: 11
           System.out.println("El valor de n es: " + n); // El valor de n es: 0
             */

            /*
             * utilizando la versión prefija (++i))? 
             */
           int i = 10;
           int n = ++i%5; // 11%5 = 1

           System.out.println("El valor de i es: " + i); // El valor de i es: 11
           System.out.println("El valor de n es: " + n); // El valor de n es: 1
    }
    
}
